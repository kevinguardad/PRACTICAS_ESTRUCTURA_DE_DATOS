#Ejercicio 5: Simulacion de registro de un estudiante universitario

#Variables del estudiante
nombre = "Ana Martinez" #str
edad = 19               #int
promedio = 8.7         #float
becado = True           #bool

#Mostrar informacion del estudiante
print("=== Regirstro de Estudiante ===")
print(f"Nombre: {nombre}")
print(f"Edad: {edad} años")
print(f"Promedio: {promedio}")
print(f"Becado: {becado}")

#Calculos con las variables numericas
print("\n=== Calculos ===")
print(f"Edad en 5 años: {edad + 5}")               #suma con int
print(f"Promedio + 1 punto extra: {promedio + 1}") #suma con float
print(f"Promedio multiplicado por 2: {promedio * 2}")