#Listas de dias de la semana
dias = ["Lunes", "Martes", "Miércoles", "Jueves", "Viernes", "Sábado", "Domingo"]

#a) Mostrar los primeros 3 dias 
print("Primeros 3 dias:", dias[:3])

#b)  Mostrar los ultimos 2 dias
print("Ultimos 2 dias:", dias[-2:])

#c) Mostrar los dias laborales (Lunes a Viernes)
print("Dias laborales:", dias[:5])

# d) Mostrar el fin de semana
print("Fin de semana:", dias[5:])

# e) Mostrar todos los dias de orden inverso
print("Dias en orden inverso:", dias [::-1])

#f) Mostrar un dia si y otro no ( dias pares)
print("Un dia si y otro no:", dias[1::2])

